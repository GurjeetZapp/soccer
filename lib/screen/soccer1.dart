class SoccerDetailScreen extends StatefulWidget {
  final String title;
  final List<Map<String, String>> sections;

  const SoccerDetailScreen({Key? key, required this.title, required this.sections}) : super(key: key);

  @override
  _SoccerDetailScreenState createState() => _SoccerDetailScreenState();
}

class _SoccerDetailScreenState extends State<SoccerDetailScreen> {
  int _currentIndex = 0;

  void _nextSection() {
    setState(() {
      if (_currentIndex < widget.sections.length - 1) {
        _currentIndex++;
      } else {
        // If we're on the last section, navigate back to the main screen
        Navigator.pop(context);
      }
    });
  }

  void _previousSection() {
    setState(() {
      if (_currentIndex > 0) {
        _currentIndex--;
      }
    });
  }

  @override
  Widget build(BuildContext context) {
    final currentSection = widget.sections[_currentIndex];

    return Scaffold(
      appBar: AppBar(
        leading: GestureDetector(
          onTap: () {
            Navigator.pop(context);
          },
          child: Icon(Icons.arrow_back_ios, color: Colors.white),
        ),
        backgroundColor: Colors.red,
        title: Text(
          widget.title,
          style: TextStyle(color: Colors.white),
        ),
      ),
      body: Padding(
        padding: const EdgeInsets.all(24.0),
        child: Stack(
          children: [
            Column(
              children: [
                Text(
                  currentSection['title']!,
                  style: TextStyle(
                    fontSize: 26,
                    fontWeight: FontWeight.bold,
                    color: Colors.green,
                  ),
                  textAlign: TextAlign.center,
                ),
                SizedBox(height: 24),
                Text(
                  currentSection['description']!,
                  style: TextStyle(fontSize: 18, fontStyle: FontStyle.italic, color: Colors.black87),
                  textAlign: TextAlign.center,
                ),
              ],
            ),
            Positioned(
              bottom: 90, // Adjust this value to position arrows above the bottom
              left: 0,
              right: 0,
              child: Row(
                mainAxisAlignment: MainAxisAlignment.spaceBetween,
                children: [
                  GestureDetector(
                    onTap: _previousSection,
                    child: Container(
                      width: 60,
                      height: 60,
                      decoration: BoxDecoration(
                        shape: BoxShape.circle,
                        color: Colors.grey.shade300,
                        border: Border.all(color: Colors.black, width: 3),
                      ),
                      child: Icon(
                        Icons.arrow_back,
                        size: 32,
                        color: Colors.black,
                      ),
                    ),
                  ),
                  Row(
                    children: List.generate(widget.sections.length, (index) {
                      return Container(
                        margin: EdgeInsets.symmetric(horizontal: 4.0),
                        width: 10,
                        height: 10,
                        decoration: BoxDecoration(
                          shape: BoxShape.circle,
                          color: _currentIndex == index ? Colors.black87 : Colors.grey.shade400,
                        ),
                      );
                    }),
                  ),
                  GestureDetector(
                    onTap: _nextSection,
                    child: Container(
                      width: 60,
                      height: 60,
                      decoration: BoxDecoration(
                        shape: BoxShape.circle,
                        color: Colors.grey.shade300,
                        border: Border.all(color: Colors.black, width: 3),
                      ),
                      child: Icon(
                        Icons.arrow_forward,
                        size: 32,
                        color: Colors.black,
                      ),
                    ),
                  ),
                ],
              ),
            ),
          ],
        ),
      ),
    );
  }
}


   Widget bulletText(String text) {
    return Row(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Text("• ", style: TextStyle(fontSize: 16)),
        Expanded(
          child: Text(
            text,
            style: TextStyle(fontSize: 16),
          ),
        ),
      ],
    );
  }


